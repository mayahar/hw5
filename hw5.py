import pathlib
import pandas as pd
import json
import numpy as np
from typing import Union, Tuple
import matplotlib.pyplot as plt

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):
        # ...
        path = pathlib.Path(data_fname)
        if path.is_file():
            self.data_fname = path
        else:
            raise ValueError

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        f = open(self.data_fname)
        self.data =  pd.DataFrame(json.load(f))
    
    #Q1

    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.
        
        Returns
        -------
        hist : np.ndarray
        Number of people in a given bin
        bins : np.ndarray
        Bin edges
        """
        age = self.data.replace("nan", np.NaN)['age']
        edges = np.linspace(0,100,11)
        hist = plt.hist(age[~np.isnan(age)], bins = edges)[0]
        return (hist,edges)
        
    #Q2

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.
        
        Returns
        -------
        df : pd.DataFrame
        A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
        the (ordinal) index after a reset.
        """
        
        df = self.data
        
        df = df[df['email'] != None]
        df = df[df['email'].str.count('@') == 1] #execty one @
        df = df[df['email'].str.find('@') != 0]  #@ not first
        df = df[df['email'].str[-1] != '@'] #@ not last
        df = df[df['email'].str.count('.') >0] #more than zero .
        df = df[df['email'].str.find('.')!=-1] #. not first
        df = df[df['email'].str.find("@.")== -1] #doesn't conrain @.

        return df.reset_index()
    
    #Q3
    def fill_na_with_mean(self) -> Tuple[pd.DataFrame, np.ndarray]:
       """Finds, in the original DataFrame, the subjects that didn't answer
       all questions, and replaces that missing value with the mean of the
       other grades for that student.

        Returns
        -------
        df : pd.DataFrame
            The corrected DataFrame after insertion of the mean grade
        arr : np.ndarray
            Row indices of the students that their new grades were generated
       """
       df = self.data
       df=df.replace("nan", np.NaN)
       q = ['q1','q2','q3','q4','q5']
       rows = [index for index, row in df[q].iterrows() if row.isnull().any()]
       arr = np.asarray(rows)
       
       avg = df[q].mean(axis = 1)
       df[q] = df[q].T.fillna(avg).T
       
       return (df, arr)
    
    #Q4
    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.

        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.

        Parameters
        ----------
        maximal_nans_per_sub : int, optional
        Number of allowed NaNs per subject before giving a NA score.

        Returns
        -------
        pd.DataFrame
        A new DF with a new column - "score".
        """
        
        df = self.data
        df=df.replace("nan", np.NaN)
        q = ['q1','q2','q3','q4','q5']
        avg = df[q].mean(axis = 1)
        avg = avg.astype(np.uint8)
        avg[df[q].isna().sum(axis=1) > maximal_nans_per_sub] = np.NaN
        avg = avg.astype('UInt8')
        df['score'] = avg

        return df

    #Q5
    def correlate_gender_age(self) -> pd.DataFrame:
        """Looks for a correlation between the gender of the subject, their age
        and the score for all five questions.

        Returns
        -------
        pd.DataFrame
        A DataFrame with a MultiIndex containing the gender and whether the subject is above
        40 years of age, and the average score in each of the five questions.

        [Average per group - result of `correlate_gender_age`](avg_per_group.png)

        """

        df = self.data.replace("nan", np.NaN)
        q = ['q1','q2','q3','q4','q5']
        df['age'] = df['age'] > 40
        df = df.groupby(['gender','age']).mean()
        return df[q]

